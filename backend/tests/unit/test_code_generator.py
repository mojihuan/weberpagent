def test_build_body_mixed_indent_multiline(
    generator: PlaywrightCodeGenerator,
) -> None:
    """INDENT-01: mixed indent + multi-line try-except, blank separator preserved."""
    actions = [
        TranslatedAction(
            code='page.goto("url")',
            action_type="navigate",
            is_comment=False,
            has_locator=False,
        ),
        TranslatedAction(
            code=(
                "    try:\n"
                '        page.locator("xpath=//button").click()\n'
                "    except Exception as _e:"
            ),
            action_type="click",
            is_comment=False,
            has_locator=True,
        ),
    ]
    result = generator._build_body(actions)
    result_lines = result.split("\n")

    # First non-empty line must start with 4 spaces
    first_line = result_lines[0]
    assert first_line == "    page.goto(\"url\")"

    # Blank separator line between action types is preserved as empty
    assert "" in result_lines

    # The already-indented multi-line block is unchanged
    assert '        page.locator("xpath=//button").click()' in result
    assert "    except Exception as _e:" in result


def test_generate_calls_validate_syntax(
    generator: PlaywrightCodeGenerator,
) -> None:
    """INDENT-03: generate() calls validate_syntax on the output."""
    actions = _make_actions()
    with patch.object(generator, "validate_syntax", return_value=True) as mock_vs:
        result = generator.generate("r1", "test", "t1", actions)

    mock_vs.assert_called_once()
    # validate_syntax was called with the content string
    call_arg = mock_vs.call_args[0][0]
    assert "def test_" in call_arg
    # generate() still returns the content
    assert "def test_" in result


def test_generate_logs_warning_on_syntax_error(
    generator: PlaywrightCodeGenerator,
) -> None:
    """INDENT-03: generate() logs warning when validate_syntax returns False."""
    actions = _make_actions()

    with (
        patch.object(generator, "validate_syntax", return_value=False),
        patch("backend.core.code_generator.ast.parse", side_effect=SyntaxError("invalid syntax", ("<test>", 5, 1, "def broken("))),
        patch("backend.core.code_generator.logger") as mock_logger,
    ):
        result = generator.generate("r1", "test", "t1", actions)

    # Warning was logged containing the Chinese validation message
    mock_logger.warning.assert_called_once()
    call_args = mock_logger.warning.call_args[0][0]
    assert "语法验证失败" in call_args
    # generate() still returns content (does not raise)
    assert "def test_" in result


async def test_generate_and_save_validates_before_write() -> None:
    """INDENT-03: generate_and_save calls validate_syntax, writes file even on failure."""
    gen = PlaywrightCodeGenerator()
    mock_history = MagicMock()
    mock_history.model_actions.return_value = [
        {"navigate": {"url": "http://x"}, "interacted_element": None},
    ]

    with tempfile.TemporaryDirectory() as tmpdir:
        with patch.object(gen, "validate_syntax", return_value=False) as mock_vs:
            code_path = await gen.generate_and_save(
                run_id="vs_test",
                task_name="验证测试",
                task_id="t_vs",
                agent_history=mock_history,
                base_dir=tmpdir,
            )

        # validate_syntax was called (once in generate() + once in generate_and_save())
        assert mock_vs.call_count == 2

        # File was still written even when validate_syntax returned False
        assert os.path.exists(code_path)
        content = Path(code_path).read_text()
        assert "def test_" in content
