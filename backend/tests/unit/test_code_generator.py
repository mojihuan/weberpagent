class TestGenerateCompleteFile:
    """完整文件生成测试。"""

    def test_generate_complete_file(self, generator: PlaywrightCodeGenerator) -> None:
        """生成包含所有操作类型的完整测试文件。"""
        actions = _make_actions()
        result = generator.generate(
            run_id="abc12345",
            task_name="销售出库流程",
            task_id="task001",
            actions=actions,
        )

        # 元数据头部 (D-04)
        assert "# Generated by aiDriveUITest" in result
        assert "# Task: 销售出库流程" in result
        assert "# Run ID: abc12345" in result
        assert "# Task ID: task001" in result
        assert "# Source: model_actions() from browser-use Agent" in result

        # Import 语句
        assert "from playwright.sync_api import Page" in result

        # 函数定义
        assert "def test_" in result

        # 操作内容
        assert "page.goto(" in result
        assert "page.locator(" in result
        assert "page.mouse.wheel(" in result
        assert "page.keyboard.press(" in result
        assert "page.go_back()" in result


class TestGeneratedCodeParses:
    """生成的代码必须是合法 Python。"""

    def test_generated_code_parses(self, generator: PlaywrightCodeGenerator) -> None:
        """ast.parse() 应成功解析生成的代码。"""
        actions = _make_actions()
        code = generator.generate(
            run_id="parse_test",
            task_name="解析测试",
            task_id="tid001",
            actions=actions,
        )

        tree = ast.parse(code)
        function_defs = [node for node in ast.walk(tree) if isinstance(node, ast.FunctionDef)]
        assert len(function_defs) >= 1


