class TestFunctionNameSanitization:
    """函数名净化测试 (D-03)。"""

    def test_chinese_task_name(self, generator: PlaywrightCodeGenerator) -> None:
        """中文任务名应保留在函数名中。"""
        code = generator.generate(
            run_id="r1",
            task_name="销售出库流程",
            task_id="t1",
            actions=[],
        )
        assert "test_销售出库流程" in code

    def test_special_chars_replaced(self, generator: PlaywrightCodeGenerator) -> None:
        """特殊字符应替换为下划线。"""
        code = generator.generate(
            run_id="r2",
            task_name="test with special! chars",
            task_id="t2",
            actions=[],
        )
        assert "!" not in code.split("def test_")[1].split("(")[0]
        assert " " not in code.split("def test_")[1].split("(")[0]

    def test_empty_task_name(self, generator: PlaywrightCodeGenerator) -> None:
        """空任务名应使用 test_unnamed。"""
        code = generator.generate(
            run_id="r3",
            task_name="",
            task_id="t3",
            actions=[],
        )
        assert "test_unnamed" in code


