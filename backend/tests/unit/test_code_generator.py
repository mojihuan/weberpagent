def test_generate_complete_file(generator: PlaywrightCodeGenerator) -> None:
    """Code generation success: full file with all action types and metadata."""
    actions = _make_actions()
    result = generator.generate(
        run_id="abc12345",
        task_name="销售出库流程",
        task_id="task001",
        actions=actions,
    )

    # Metadata header
    assert "# Generated by aiDriveUITest" in result
    assert "# Task: 销售出库流程" in result
    assert "# Run ID: abc12345" in result

    # Import + function definition
    assert "from playwright.sync_api import Page" in result
    assert "def test_" in result

    # All action types present
    assert "page.goto(" in result
    assert "page.locator(" in result
    assert "page.mouse.wheel(" in result
    assert "page.keyboard.press(" in result
    assert "page.go_back()" in result


def test_fallback_complete_file_parses(generator: PlaywrightCodeGenerator) -> None:
    """Multi-opcode with fallback locators: try-except code produces valid Python."""
    actions = _make_fallback_actions()
    code = generator.generate(
        run_id="fb06",
        task_name="完整回退",
        task_id="t_fb06",
        actions=actions,
    )
    tree = ast.parse(code)
    func_defs = [n for n in ast.walk(tree) if isinstance(n, ast.FunctionDef)]
    assert len(func_defs) == 1
    assert "import logging" in code


async def test_healing_failure_preserves_original(generator: PlaywrightCodeGenerator) -> None:
    """Error handling: LLM healing failure still generates valid Python file."""
    mock_history = MagicMock()
    mock_history.model_actions.return_value = [
        {"click": {"index": 5}, "interacted_element": None},
    ]

    with tempfile.TemporaryDirectory() as tmpdir:
        run_id = "heal_test_6"
        dom_dir = Path(tmpdir) / run_id / "dom"
        dom_dir.mkdir(parents=True, exist_ok=True)
        (dom_dir / "step_1.txt").write_text("<html><button>Submit</button></html>")

        with patch("backend.core.code_generator.LLMHealer") as mock_cls:
            mock_instance = AsyncMock()
            mock_instance.heal.return_value = MagicMock(
                success=False,
                code_snippet="",
                raw_response="timeout",
                locator="",
            )
            mock_cls.return_value = mock_instance

            code_path = await generator.generate_and_save(
                run_id=run_id,
                task_name="失败修复",
                task_id="t_heal6",
                agent_history=mock_history,
                base_dir=tmpdir,
                llm_config={"model": "test", "api_key": "key"},
            )

            content = Path(code_path).read_text()
            ast.parse(content)
            assert os.path.exists(code_path)


