        print(f"开始检查，总共 {len(self.positioning)} 个元素...")
        found_count = 0
        for key, value_list in self.positioning.items():
            # 判断列表中所有元素是否都为空字符串
            if all(item == "" for item in value_list):
                print(f"❌ 发现全空 Key: '{key}' -> {value_list}")
                found_count += 1

        if found_count == 0:
            print("✅ 未发现所有 value 均为空字符串的 Key。")
        else:
            print(f"共发现 {found_count} 个全空 Key。")


